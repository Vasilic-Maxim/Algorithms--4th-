package chapter1.section1;

public class Exercise_4 {
    public static void main(String[] args) {
        /*
        a. if (a > b) then c = 0;
        There is no such statement as 'then' in Java. Corrected
        version: if (a > b) c = 0;

        b. if a > b { c = 0; }
        Condition (a > b) must be surrounded by parentheses. Right
        way to do it: if (a > b) { c = 0; }

        c. if (a > b) c = 0;
        And we have the winner! That actually is one of the right
        ways to write that IF statement.

        d. if (a > b) c = 0 else b = 0;
        The semicolon is not present after IF statement. Should be:
        if (a > b) c = 0; else b = 0;
        */
    }
}
